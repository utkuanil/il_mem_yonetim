class DashboardSummary {
  final int schoolCount;
  final int studentCount;
  final int teacherCount;
  final int activeProjectCount;
  final int pendingApplicationCount;
  final int openRequestCount;
  final int plannedInspectionCount;
  final int urgentAnnouncementCount;

  const DashboardSummary({
    required this.schoolCount,
    required this.studentCount,
    required this.teacherCount,
    required this.activeProjectCount,
    required this.pendingApplicationCount,
    required this.openRequestCount,
    required this.plannedInspectionCount,
    required this.urgentAnnouncementCount,
  });
}
